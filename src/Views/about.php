<h2>Sobre Nosotros</h2>
<section>
    <h3>Nuestra Historia</h3>
    <p>PAWprints nació de la pasión por la lectura y el deseo de crear un espacio donde las historias dejen una marca duradera en los lectores. Lo que comenzó como un proyecto enfocado en la curaduría de títulos indispensables, se transformó en una librería digital dedicada a conectar a las personas con libros que inspiran, educan y entretienen. Con una identidad visual vibrante y moderna, hemos construido un catálogo que refleja la diversidad de voces y géneros que definen el mundo literario actual.</p>
